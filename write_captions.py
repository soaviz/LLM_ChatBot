count = 0
for post_id, caption in captions.items():
    old = f'id:\'{post_id}\', image:'
    if old not in html:
        print(f"WARNING: {post_id} not found")
        continue
    # Find the caption field for this post and replace it
    # Pattern: caption:"...", date:
    import re
    pattern = f"(id:'{post_id}', image:'[^']+', caption:)\"[^\"]*\""
    replacement = f'\\1"{caption}"'
    new_html, n = re.subn(pattern, replacement, html)
    if n == 1:
        html = new_html
        count += 1
    else:
        print(f"ERROR: {post_id} matched {n} times")

print(f"Updated {count} captions")

